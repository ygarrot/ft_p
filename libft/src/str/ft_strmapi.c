#include <stdlib.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	i = 0;
	str = NULL;
	if (!(s || (*f)))
		return (NULL);
	while (s[i])
		i++;
	if ((str = (char*)malloc(i + 1 * sizeof(char))) == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
