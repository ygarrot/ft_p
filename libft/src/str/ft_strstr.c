#include <string.h>

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	if (!s2[i])
		return ((char*)s1);
	while (s1[i])
	{
		i2 = 0;
		while (s1[i + i2] == s2[i2])
		{
			i2++;
			if (!s2[i2])
				return ((char*)(&s1[i]));
		}
		i++;
	}
	return (0);
}
