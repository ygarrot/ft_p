#include "libft.h"

int		ft_mcharchr(char *str, char *c)
{
	int i;

	i = 0;
	while (str && str[i] && !ft_isin(str[i], c))
		i++;
	return ((str && str[i] && ft_isin(str[i], c)) ? i : -1);
}
