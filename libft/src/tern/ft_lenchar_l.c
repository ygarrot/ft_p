#include "libft.h"

int		ft_lenchar_l(char *str, int pos)
{
	int	i;

	if (pos == 0 || !str)
		return (0);
	i = 1;
	while (pos - i >= 0 && (str[pos - i] & '\xc0') == '\x80')
		i++;
	return (i);
}
