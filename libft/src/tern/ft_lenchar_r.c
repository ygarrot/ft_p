#include "libft.h"

int		ft_lenchar_r(char *str, int pos)
{
	int	i;

	if (!str || !str[pos])
		return (0);
	i = 1;
	while (str[pos + i] && (str[pos + i] & '\xc0') == '\x80')
		i++;
	return (i);
}
