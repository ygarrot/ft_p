#include "libft.h"

int		ft_isin(char c, char *str)
{
	size_t		i;

	i = 0;
	if (!str || !c)
		return (0);
	while (str[i] && str[i] != c)
		i++;
	return ((!str[i] || str[i] != c) ? 0 : 1);
}
