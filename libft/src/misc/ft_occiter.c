#include "libft.h"

int	ft_occiter(char *str, int op, int (*f)(int, int))
{
	int	cp;

	cp = 0;
	if (!str)
		return (0);
	while (*str && ((*f)(*str++, op)))
		cp++;
	return (cp);
}
